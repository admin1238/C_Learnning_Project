#include <stdio.h>
#include "problems.h"

void problem_016(void)
{
    printf("===== 第16题：求最大公倍数和最大公因数 =====\n");
    // 在这里编写你的题目代码
    // 最小公倍数 × 最大公约数 = 这两个数的乘积
    // 辗转相除法 
    int m ,n,a,b;
    int gcd ,lcm;
    int temp;

     printf("请输入两个正整数 m 和 n:");
    scanf("%d %d", &m, &n);

    // 用 a 和 b 来备份原始数据，因为后面计算中 m 和 n 会被修改
    a = m;
    b = n;

    // ---------- 1. 辗转相除法求最大公约数 (GCD) ----------
    // 核心思想：只要余数不为0，就不断用 除数 去除 余数
    while (b != 0) {
        temp = a % b;  // 计算余数
        a = b;         // 把除数变成新的被除数
        b = temp;      // 把余数变成新的除数
    }
    gcd = a; // 当 b 为 0 时，a 中存放的就是最大公约数

    // ---------- 2. 利用公式求最小公倍数 (LCM) ----------
    // 注意：为了防止 m * n 过大导致 int 溢出（比如 m=100000, n=99999）
    // 严谨的写法是先除以 gcd，再乘以另一个数
    lcm = (m / gcd) * n; 

    // ---------- 3. 输出结果 ----------
    printf("最大公约数 (GCD) = %d\n", gcd);
    printf("最小公倍数 (LCM) = %d\n", lcm);

}

